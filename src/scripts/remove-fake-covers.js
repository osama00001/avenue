async function run() {
  await connectDB();
  
  // Create a regex to match ANY coverImage URL that contains one of the fake ISBNs
  const fakeRegex = new RegExp(FAKE_ISBNS.join('|'));
  
  // We also remove covers that literally say "placeholder"
  const res = await Book.updateMany(
    { 
      $or: [
        { coverImage: { $regex: fakeRegex } },
        { coverImage: { $regex: 'placeholder' } }
      ]
    },
    { $unset: { coverImage: "" } }
  );
  
  console.log('SUCCESS: Unlinked fake covers for', res.modifiedCount, 'books.');
  process.exit(0);
}

run().catch(console.error);
